int minimumSwaps(vector<int> arr) {

    int i, j, swapcount=0, tmpval, nSize = arr.size();
    arr.emplace(arr.begin(),0); // insert 0 and make array 1-based;
        
    for (i=1; i<=nSize; i++)// for all i starting with 1
    {   // find i in array;
        j = i;
        while (arr[j]!=i)
            j++;
             // found it!
        while (arr[j]!= j) // while arr[j]!=j, swap arr[j] to its correct index
        { // find i in the vector arr
            tmpval = arr[arr[j]];
            arr[arr[j]] = arr[j];
            arr[j]=tmpval;
            swapcount++;
        }       
    }
    return swapcount;
}

